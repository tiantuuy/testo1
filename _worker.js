const ФІКСОВАНИЙ_ІДЕНТИФІКАТОР = 'd14bd0e0-9ade-4824-aa96-03bbe680b4db';
let зворотній_сервер = 'yx1.9898981.xyx8443';

export default {
    async fetch(запит) {
        try {
            const заголовокОновлення = запит.headers.get('Upgrade');

            if (заголовокОновлення !== 'websocket') {
                return new Response('Привіт Світ!', { status: 200 });
            }

            зворотній_сервер = зворотній_сервер
                ? зворотній_сервер
                : запит.cf.colo + '.proxyIP.cmliuSSSS.NET';

            const [адресаЗворотньогоСервера, портЗворотньогоСервера] =
                await визначенняАдресиПорту(зворотній_сервер);

            return await обробкаВебСокету(запит, {
                серверПеренаправлення: адресаЗворотньогоСервера,
                портПеренаправлення: портЗворотньогоСервера
            });

        } catch (помилка) {
            return new Response(
                помилка?.stack || String(помилка),
                { status: 500 }
            );
        }
    },
};

async function обробкаВебСокету(запит, налаштування) {

    const { серверПеренаправлення, портПеренаправлення } = налаштування;

    const вебСокетПара = new WebSocketPair();
    const [клієнтськийВебСокет, сервернийВебСокет] =
        Object.values(вебСокетПара);

    сервернийВебСокет.accept();

    let інтервалСерцебиття = setInterval(() => {
        if (сервернийВебСокет.readyState === СТАН_ВІДКРИТОГО_ВЕБСОКЕТУ) {
            try {
                сервернийВебСокет.send(new Uint8Array(0));
            } catch {}
        }
    }, 30000);

    function очищенняСерцебиття() {
        if (інтервалСерцебиття) {
            clearInterval(інтервалСерцебиття);
            інтервалСерцебиття = null;
        }
    }

    сервернийВебСокет.addEventListener('close', очищенняСерцебиття);
    сервернийВебСокет.addEventListener('error', очищенняСерцебиття);

    const ранніДаніЗаголовка =
        запит.headers.get('sec-websocket-protocol') || '';

    const читабельнийВебСокет =
        створенняЧитабельногоПотокуВебСокет(
            сервернийВебСокет,
            ранніДаніЗаголовка
        );

    let віддаленийСокет = null;
    let записUdpПотоку = null;
    let цеDns = false;

    читабельнийВебСокет.pipeTo(new WritableStream({

        async write(фрагмент) {

            if (цеDns && записUdpПотоку) {
                return записUdpПотоку(фрагмент);
            }

            if (віддаленийСокет) {
                const w = віддаленийСокет.writable.getWriter();
                await w.write(фрагмент);
                w.releaseLock();
                return;
            }

            const результат = аналізЗаголовкаVLESS(фрагмент);
            if (результат.єПомилка)
                throw new Error(результат.повідомлення);

            if (результат.адресаВіддаленого.includes(
                atob('c3BlZWQuY2xvdWRmbGFyZS5jb20=')
            ))
                throw new Error('Доступ заборонено');

            const відповідьVless =
                new Uint8Array([результат.версіяVless[0], 0]);

            const сиріДаніКлієнта =
                фрагмент.slice(результат.індексСиріхДаних);

            if (результат.цеUDP) {
                if (результат.портВіддаленого === 53) {
                    цеDns = true;
                    const { write } =
                        await обробкаUdpВиходу(
                            сервернийВебСокет,
                            відповідьVless
                        );
                    записUdpПотоку = write;
                    записUdpПотоку(сиріДаніКлієнта);
                    return;
                }
                throw new Error('UDP лише DNS');
            }

            async function зєднання(адреса, порт) {
                const tcpСокет = await connect(
                    { hostname: адреса, port: порт },
                    { allowHalfOpen: true }
                );

                віддаленийСокет = tcpСокет;

                const w = tcpСокет.writable.getWriter();
                await w.write(сиріДаніКлієнта);
                w.releaseLock();

                return tcpСокет;
            }

            try {

                // 直连
                let tcpСокет = await зєднання(
                    результат.адресаВіддаленого,
                    результат.портВіддаленого
                );

                передачаДанихДоВебСокету(
                    tcpСокет,
                    сервернийВебСокет,
                    відповідьVless,
                    {
                        типАдреси: результат.типАдреси,
                        адреса: результат.адресаВіддаленого,
                        порт: результат.портВіддаленого,
                        дані: сиріДаніКлієнта,
                        серверПеренаправлення,
                        портПеренаправлення
                    }
                );

            } catch {

                // 回退 Worker
                const tcpСокет = await зєднання(
                    серверПеренаправлення,
                    портПеренаправлення
                );

                передачаДанихДоВебСокету(
                    tcpСокет,
                    сервернийВебСокет,
                    відповідьVless,
                    {
                        типАдреси: результат.типАдреси,
                        адреса: серверПеренаправлення,
                        порт: портПеренаправлення,
                        дані: сиріДаніКлієнта,
                        серверПеренаправлення,
                        портПеренаправлення
                    }
                );
            }

        },

        close() {
            закриттяСокета(віддаленийСокет);
        }

    })).catch(() => {
        закриттяСокета(віддаленийСокет);
        if (сервернийВебСокет.readyState === СТАН_ВІДКРИТОГО_ВЕБСОКЕТУ)
            сервернийВебСокет.close(1011);
    });

    return new Response(null, {
        status: 101,
        webSocket: клієнтськийВебСокет
    });
}
